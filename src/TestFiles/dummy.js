function addNumbers(a, b) {
    return a + b;
}

function multiplyNumbers(a, b) {
    return a * b;
}

function greet(name) {
    return `Hello, ${name}!`;
}

// Test cases
console.log(addNumbers(2, 3)); // Output: 5
console.log(multiplyNumbers(4, 5)); // Output: 20
console.log(greet("John")); // Output: Hello, John!
